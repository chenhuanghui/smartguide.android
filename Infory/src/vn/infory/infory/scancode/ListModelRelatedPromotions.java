package vn.infory.infory.scancode;

import java.util.ArrayList;

public class ListModelRelatedPromotions {
	private ArrayList<Integer> shop_ids;
	private String name;
	private String logo;
	private String time;
	private String description;
	public ArrayList<Integer> getShop_ids() {
		return shop_ids;
	}
	public void setShop_ids(ArrayList<Integer> shop_ids) {
		this.shop_ids = shop_ids;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
